package com.maxwittig.genericpuzzle.logic;


public class PuzzleSize
{
    private double width;
    private double height;

    public PuzzleSize(double width, double height)
    {
        this.width = width;
        this.height = height;
    }

    public double getWidth()
    {
        return width;
    }

    public double getHeight()
    {
        return height;
    }
}
